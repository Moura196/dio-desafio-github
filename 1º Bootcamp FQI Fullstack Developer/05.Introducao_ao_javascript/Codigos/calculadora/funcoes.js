// TIPOS DE FUNÇÃO

// DECLARATIVAS

function funcao() {
    console.log('Sou uma função declarativa')
}

funcao();

// ESPRESSAO DE FUNÇÃO

// COM NOMEAÇÃO

var funcao = function() {
    console.log('Sou uma função de expressão')
}

// ARROW FUNCTION

var funcao = () => {
    console.log('Sou uma arrow function')
}

funcao();